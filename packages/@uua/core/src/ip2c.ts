export default function detectCountry(): Promise<UAACountry> {
  return axios.get<string>('https://ip2c.org/s').then(({ data = '' }) => {
    // 1;CN;CHN;China
    let code = '';
    let country = '';
    let icon = '/flag/no-flag.png';
    if (data.charAt(0) === '1') {
      [, code = 'unkown', , country = 'Unkown'] = data.split(';');
      const name = code.toLowerCase();
      if (~icons.indexOf(name)) {
        icon = `/flag/${name}.png`;
      }
    }
    return {
      code,
      country,
      icon
    };
  });
}
